        public static void ExportGridView(Control gv, System.Web.UI.Page pg)
        {
            
            PrepareGridViewForExport(gv);
            string attachment = "attachment; filename=Report_" + DateTime.Now.Day.ToString() + "_" + DateTime.Now.Month.ToString() + "_" + DateTime.Now.Year.ToString() + "_" + DateTime.Now.Ticks.ToString() + ".xls";
            pg.Response.ClearContent();
            pg.Response.AddHeader("content-disposition", attachment);
            pg.Response.ContentType = "application/ms-excel";
            StringWriter sw = new StringWriter();
            HtmlTextWriter htw = new HtmlTextWriter(sw);
            gv.RenderControl(htw);
            pg.Response.Write(sw.ToString());
            pg.Response.End();

        }

